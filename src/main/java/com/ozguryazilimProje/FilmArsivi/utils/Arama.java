package com.ozguryazilimProje.FilmArsivi.utils;

public class Arama {

		private String aranan;
		private String tur;
		
		public Arama(String aranan, String tur) {
			this.aranan = aranan;
			this.tur = tur;
		}
		public String getAranan() {
			return aranan;
		}
		public void setAranan(String aranan) {
			this.aranan = aranan;
		}
		public String getTur() {
			return tur;
		}
		public void setTur(String tur) {
			this.tur = tur;
		}
	
}
